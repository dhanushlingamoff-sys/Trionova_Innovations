        function activateTab(targetBtn) {
            tabs.forEach(btn => btn.setAttribute('aria-selected', 'false'));
            targetBtn.setAttribute('aria-selected', 'true');

            const key = targetBtn.dataset.tab;
            const entry = data[key];
            if (!entry) return;

            // small fade animation
            cardTitle.classList.add('fade-enter');
            cardDesc.classList.add('fade-enter');
            cardMedia.classList.add('fade-enter');

            // replace content after a tiny timeout so animation triggers
            setTimeout(() => {
                cardTitle.textContent = entry.title;
                cardDesc.textContent = entry.desc;
                cardMedia.src = entry.img;

                // remove classes to trigger CSS transition
                cardTitle.classList.remove('fade-enter');
                cardDesc.classList.remove('fade-enter');
                cardMedia.classList.remove('fade-enter');
            }, 60);
        }

        tabs.forEach(btn => {
            btn.addEventListener('click', () => activateTab(btn));
            btn.addEventListener('keydown', (e) => {
                if (e.key === 'Enter' || e.key === ' ') {
                    e.preventDefault();
                    activateTab(btn);
                }
            });
        });

        // keyboard navigation (up/down)
        document.querySelector('.ui-tabs').addEventListener('keydown', (e) => {
            const focusable = Array.from(tabs);
            const activeIndex = focusable.findIndex(t => t.getAttribute('aria-selected') === 'true');
            if (e.key === 'ArrowDown') {
                e.preventDefault();
                const next = focusable[(activeIndex + 1) % focusable.length];
                next.focus();
            }
            if (e.key === 'ArrowUp') {
                e.preventDefault();
                const prev = focusable[(activeIndex - 1 + focusable.length) % focusable.length];
                prev.focus();
            }
        });
