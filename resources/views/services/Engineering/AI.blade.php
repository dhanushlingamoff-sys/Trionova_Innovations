            // Ensure section can contain absolute element
            $section.css('position', 'relative');

            function resetSticky() {
                $sticky.css({
                    position: '',
                    top: '',
                    left: '',
                    width: ''
                }).removeClass('sticky-fixed sticky-absolute');
            }

            function updateSticky() {

                // Disable on small screens
                if ($win.width() < 992) {
                    resetSticky();
                    return;
                }

                var scrollTop = $win.scrollTop();
                var sectionTop = $section.offset().top;
                var sectionHeight = $section.outerHeight();
                var stickyHeight = $sticky.outerHeight();
                var winHeight = $win.height();

                // Prevent breaking if sticky is too tall
                if (stickyHeight + topOffset > winHeight) {
                    resetSticky();
                    return;
                }

                var leftOffset = $leftCol.offset().left;
                var leftRelative = $leftCol.position().left;
                var colWidth = $leftCol.outerWidth();

                var fixStart = sectionTop - topOffset;

                // 👇 EARLY STOP POINT (IMPORTANT FIX)
                var fixEnd = (sectionTop + sectionHeight) - stickyHeight - stopOffset;

                if (scrollTop >= fixStart && scrollTop <= fixEnd) {
                    // FIXED STATE
                    $sticky.css({
                        position: 'fixed',
                        top: topOffset + 'px',
                        left: leftOffset + 'px',
                        width: colWidth + 'px'
                    }).removeClass('sticky-absolute').addClass('sticky-fixed');

                } else if (scrollTop > fixEnd) {
                    // ABSOLUTE STATE (STOPS EARLY)
                    $sticky.css({
                        position: 'absolute',
                        top: (sectionHeight - stickyHeight - stopOffset) + 'px',
                        left: leftRelative + 'px',
                        width: colWidth + 'px'
                    }).removeClass('sticky-fixed').addClass('sticky-absolute');

                } else {
                    // NORMAL STATE
                    resetSticky();
                }
            }

            // Scroll (RAF throttled)
            $win.on('scroll', function() {
                if (!ticking) {
                    ticking = true;
                    requestAnimationFrame(function() {
                        updateSticky();
                        ticking = false;
