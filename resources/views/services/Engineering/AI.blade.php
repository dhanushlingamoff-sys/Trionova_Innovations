        $(function() {

            var $win = $(window);
            var $section = $('.sticky-container-section').first();
            var $leftCol = $section.find('.col-lg-6').first();
            var $sticky = $leftCol.find('.sticky-content').first();

            var topOffset = 100;
            var stopOffset = 90;
            var ticking = false;

